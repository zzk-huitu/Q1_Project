package com.yc.q1.pojo.base.version;

public class VersionInfo {

	private String mainLogo;
	private String smallLogo;
	private String nameLogo;
	private String clientName;
	private String serviceName;
	private String varsionName;
	
	public String getMainLogo() {
		return mainLogo;
	}
	public void setMainLogo(String mainLogo) {
		this.mainLogo = mainLogo;
	}
	public String getSmallLogo() {
		return smallLogo;
	}
	public void setSmallLogo(String smallLogo) {
		this.smallLogo = smallLogo;
	}
	public String getNameLogo() {
		return nameLogo;
	}
	public void setNameLogo(String nameLogo) {
		this.nameLogo = nameLogo;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getVarsionName() {
		return varsionName;
	}
	public void setVarsionName(String varsionName) {
		this.varsionName = varsionName;
	}
}
